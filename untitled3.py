# Ініціалізація словника для обліку читача
читач = {
    "ID": 16,
    "прізвище": "Марина",
    "Ім’я": "Юрій",
    "Група": "ІПЗс-23-2",
    "Курс": 1,
    "Книги": [],
    "Статистика книг": []
}

# Функція для виведення карти читача
def вивести_карту_читача(читач):
    print("Карта читача:")
    for ключ, значення in читач.items():
        if ключ != "Книги" and ключ != "Статистика книг":
            print(f"{ключ}: {значення}")

# Основний цикл програми
while True:
    print("Оберіть опцію:")
    print("1 - Вивести карту читача")
    print("2 - Взяти книгу")
    print("3 - Повернути книгу")
    print("0 - Вийти з програми")
    вибір = input()

    if вибір == "1":
        вивести_карту_читача(читач)
    elif вибір == "2":
        назва_книги = input("Введіть назву книги, яку ви бажаєте взяти: ")
        читач["Книги"].append(назва_книги)
        print(f"Книга '{назва_книги}' додана до списку боргу.")
    elif вибір == "3":
        if len(читач["Книги"]) == 0:
            print("Список боргу порожній.")
        else:
            print("Список книг у боргу:")
            for книга in читач["Книги"]:
                print(книга)
            назва_книги = input("Введіть назву книги, яку ви бажаєте повернути: ")
            if назва_книги in читач["Книги"]:
                читач["Книги"].remove(назва_книги)
                читач["Статистика книг"].append(назва_книги)
                print(f"Книгу '{назва_книги}' повернуто та внесено до списку статистики.")
            else:
                print("Книга не знайдена у списку боргу.")
    elif вибір == "0":
        break
    else:
        print("Неправильний вибір. Будь ласка, виберіть іншу опцію.")
